public class TitleDetailItem {

    private String title;

    private String detail;



    public TitleDetailItem(String title, String detail) {
        this.title = title;
        this.detail = detail;
    }

    public String getTitle() {
        return title;
    }

    public String getDetail() {
        return detail;
    }

}
